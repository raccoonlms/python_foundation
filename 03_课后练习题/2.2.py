# 2. 集合（set）
# 1. 创建两个集合 set1 = {1, 3, 5, 7} ，set2 = {5, 7, 9, 11} ，求它们的交集并打印。
set1 = {1, 3, 5, 7}
set2 = {5, 7, 9, 11}
print(set1 & set2)
# 2. 创建集合 set3 = {2, 4, 6} ，向集合中添加元素 8 并打印集合。
set3 = {2, 4, 6}
set3.add(8)
print(set3)
# 3. 创建集合set4 = {12, 14, 16, 18} ，删除集合中的元素 16 并打印集合。
set4 = {12, 14, 16, 18}
set4.remove(16)
print(set4)
# 4. 创建集合set5 = {1, 2, 3} ，set6 = {3, 4, 5} ，求它们的并集并打印。
set5 = {1, 2, 3}
set6 = {3, 4, 5}
print(set5 | set6)
# 5. 创建集合set7 = {10, 20, 30} ，判断元素 20 是否在集合中，打印判断结果。
set7 = {10, 20, 30}
print(20 in set7)
# 6. 解释集合中元素的特性（唯一性、无序性等），并通过示例说明（比如创建包含重复元素的列表，转换为集合后观察结果）。
# 集合中的元素是唯一的，并且无序的。比如创建包含重复元素的列表：
list1 = [1, 2, 3, 4, 5, 1, 2, 3]
set1 = set(list1)
print(set1)
# 输出结果为：{1, 2, 3, 4, 5}，说明列表中的元素是唯一的，并且无序的。
