# 三、数据类型练习题（识别与区分）
#  1. 请判断以下值的数据类型，并通过
# type() 函数验证（将结果打印出来）：
# 66 、 3.14 、 "Python" 、True 、 None （仅判断前 5 个的基础类型）
print(type(66))
print(type(3.14))
print(type("Python"))
print(type(True))
print(type(None))
# 2. 编写代码完成以下操作：
# 定义一个字符串变量 message ，值为 “Python 基础很重要！”
message = "Python 基础很重要！"
# 定义一个整数变量 num ，值为 2025
num = 2025
# 定义一个浮点数变量 price ，值为 99.9
price = 99.9
# 分别打印三个变量的值及其数据类型，格式为 “变量值：数据类型”（如 “Python 基础很重要！：<class'str'>”）
print(message, type(message))
print(num, type(num))
print(price, type(price))
