print(f"除法运算 = {num1 / num2}")
print(f"取模/取余运算 = {num1 % num2}")
print(f"整除运算 = {num1 // num2}")
print(f"幂运算 = {num1 ** num2}")
print(f"小括号={num1+num2*10}")
print(f"小括号={(num1+num2)*10}")

