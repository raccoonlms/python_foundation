# 二、变量练习题（定义与使用）
# 1. 按要求定义变量并输出：
# 定义变量
# student_name ，存储值为你自己的姓名（字符串类型）
student_name = "李明树"
# 定义变量
# student_age ，存储值为你的年龄（整数类型）
student_age = 21
# is_student ，存储值为 True （布尔类型）
is_student = True
# average_score ，存储值为 88.5（浮点数类型）
average_score = 88.5
# 打印所有变量，格式为 “变量名：变量值”（如 “student_name：张三”）
print(f"姓名：{student_name},年龄：{student_age},是否是学生：{is_student},平均分：{average_score}")

